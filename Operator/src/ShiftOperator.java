public class ShiftOperator {
    public static void main(String[] args) {

        //시프트 연산자
        //<<
        System.out.println(3<<1);
        System.out.println(-3<<1);
        System.out.println(3<<2);
        System.out.println(-3<<2);
        System.out.println();

        //>>
        System.out.println(5>>1);
        System.out.println(-5>>1);
        System.out.println(5>>2);
        System.out.println(-5>>2);
        System.out.println(5>>3);
        System.out.println(-5>>3);
        System.out.println();


        //>>>
        System.out.println(3>>>1);
        System.out.println(-3>>>31);
    }
}
